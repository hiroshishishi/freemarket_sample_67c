$(document).on('turbolinks:load', function() {
  $(function(){
    $('.exhibiting-btn').click(function(){
      if(!$(this).hasClass('active')){
        $('.trading-btn').removeClass('active');
        $('.sell-btn').removeClass('active');
        $(this).addClass('active');
      }
    });
    $('.trading-btn').click(function(){
      if(!$(this).hasClass('active')){
        $('.sell-btn').removeClass('active');
        $('.exhibiting-btn').removeClass('active');
        $(this).addClass('active');
      }
    });
    $('.sell-btn').click(function(){
      if(!$(this).hasClass('active')){
        $('.exhibiting-btn').removeClass('active');
        $('.trading-btn').removeClass('active');
        $(this).addClass('active');
      }
    });
    $('.buying-btn').click(function(){
      if(!$(this).hasClass('active')){
        $('.deal-btn').removeClass('active');
        $(this).addClass('active');
      }
    });
    $('.deal-btn').click(function(){
      if(!$(this).hasClass('active')){
        $('.buying-btn').removeClass('active');
        $(this).addClass('active');
      }
    });
  });
});